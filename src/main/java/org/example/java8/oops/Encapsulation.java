package org.example.java8.oops;

public class Encapsulation {

	private int data1;
	private String info1;

	public void setData1(int i) {
		data1 = i;
	}

	public int getData1() {
		return data1;
	}

	public void setInfo1(String s) {
		info1 = s;
	}

	public String getInfo1() {
		return info1;
	}
}
